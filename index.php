<td><b>Gltn is a revolution in how you write a paper.</b><br>
<span class="des">When you write a paper, you want to focus on the content. Use good sentence structures, apply strong transitions, etc. Why then, do you spend so much of your time formatting it? Gltn makes the paper modular. You focus on writing it and building a simple structure. Gltn returns it fully formatted.	</span><br>
<ul>
<li>Use a simple GUI to add citations, images, and other types of content to a document.</li>
<li>Manage citations, notes, or anything else quickly using panels alongside your content.</li>
<li>Receive a fully formatted paper in a few seconds. Then, directly start printing it. It's literally that simple.</li>
<li>Insert metadata into simple input fields without having to comb through templates.</li>
<li>Add a special character to your paper, using 0 mouse clicks.</li>
<li>Develop your own panels and/or formats for it. Make them native features. Gltn is open source and <a href="https://github.com/Fleker/Gluten">available on GitHub.</a> Check out the Wiki for quick guides to the frameworks.</li>
<a href="edit.php?file=abc">Try it out.</a>
<br><br>
<span style="font-size:9pt">Gltn is still in development. Some parts of it may drastically change as it continues to grow and add more features. Currently you may try an alpha version. Not everything may work all the time. Alert <a href="mailto:handnf+gluten@gmail.com">the developer</a> (preferrably with a console output) if there is a bug or feature you want.</span>
</ul>
