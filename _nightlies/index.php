    	<h1>Knowledge<br></h1><h3>Remembering Isidore of Seville</h3>
        <div class="center"><img src="images/dictionary.png" class="promo"></div>
        <h4><b>Dictionaries</b>- Isidore's encyclopedia recorded plenty of the world's knowledge at the time. Today, the amount of information online is incredible. Gltn 1.4, (codenamed Isidore), helps users in the pursuit of knowledge through a tool for quick information. Users may also obtain new sources from the Gltn Store. Have an article from the web? Gltn can fill in article metadata just from the URL!</h4>
