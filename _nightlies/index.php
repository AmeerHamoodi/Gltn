    <div id="citations" class="page row" style="background-color:#03a9f4;">
    	<h1>Citations<br></h1><h3>Move over RefWorks and EasyBib; It's time for innovation</h3>
        <div class="center"><img src="images/citations.png" class="promo"></div>
        <h4><b>Popups</b>- When you cite a source, you link the source to your citation. This allows the system to intelligently add inline citations as well as a bibliography. Of course, it's all formatted perfectly. It just works. Imagine all the time and effort you will save by not worrying about pointless things like missing a period in a source or not putting the title in quotes.<br><br>
        There are plenty of rules in citations. What if you have two articles from the same author? Or one article has three authors? Don't worry. Gltn can handle it automatically.
        </h4>
    </div>
    <div id="intelligence" class="page row" style="background-color:#ff5722;">
    	<h1>Intelligence<br></h1><h3>Let's Have the Editor be as Smart as You</h3>
        <div class="center"><img src="images/name_collage2.png" class="promo"></div>
        <h4><b>Citatins</b>- Spreadsheet software is powerful. It lets you analyze the input and format the output the way you want. Why hasn't this been adapted to word processing? There's plenty of rules about inline citations. What if an author writes two different articles? What if an article has three authors? Gltn knows how to deal with these scenarios and does it with ease.</h4>
    </div>
    <div id="panels" class="page row" style="background-color:#259b24;">
