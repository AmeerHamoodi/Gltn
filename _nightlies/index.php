        <div class="center"><img src="images/ideapanel3.png" class="promo"></div>
        <h4><b>Idea Panel</b>- You can write down notes for each source when you're doing research. Later, you can elaborate on your ideas while looking at your original notes.<br><br>Gltn comes with many panels built-in, and more can be installed, giving users a wide range of actions and information.</h4>
    </div>
