    	<h1>Web App<br></h1><h3>All the Power of an App, but Online</h3>
        <div class="center"><img src="images/upload2.png" class="promo"></div>
        <h4><b>Cross-Platform</b>- The beauty of the web is being cross-platform; write once, run everywhere. With browsers improving their web technologies constantly, it is actually possible to build and use a full-fledged document editor online. Plus, it takes advantage of many new features such as FileReader, LocalStorage, ApplicationCache, Web Components, new form events, along with a few external libraries that are icing on top of the cake.
        <br><br>That's not all. Yo can access and save files to all kinds of online services. Google Drive? Box? OneDrive? FTP? All accessible. Sync your files with any or all of these services.
        </h4>
